import java.util.Map;

public class Bean {

	private int i;
	private String s;
	private Map<String, String> m;
	private TT tt;

	public int getI() {
		return i;
	}

	public void setI(int i) {
		this.i = i;
	}

	public String getS() {
		return s;
	}

	public void setS(String s) {
		this.s = s;
	}

	public Map<String, String> getM() {
		return m;
	}

	public void setM(Map<String, String> m) {
		this.m = m;
	}

	public TT getTt() {
		return tt;
	}

	public void setTt(TT tt) {
		this.tt = tt;
	}

}
